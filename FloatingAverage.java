package com.variabletask;

public class FloatingAverage {
	public static void main(String[] args) {
		double firstNum=2.5;
		double secondNum=3.2;
		double thirdNum=4.7;
		double average=(firstNum+secondNum+thirdNum)/3;
		System.out.println("The average of three floating point numbers is:"+average);
	}

}
